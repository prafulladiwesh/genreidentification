df = pd.read_csv('/content/drive/My Drive/feature_lables.csv')
df.shape

sent_1=df["Senti_S1"].to_list()
sent_2=df["Senti_S2"].to_list()
sent_3=df["Senti_S3"].to_list()
sent_4=df["Senti_E1"].to_list()
sent_5=df["Senti_E2"].to_list()
