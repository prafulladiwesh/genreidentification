X_train = np.array(X_train)
y_train = np.array(y_train)

scores = []
model = svm.SVC(kernel='linear')
skfold = StratifiedKFold(n_splits=10, random_state=42, shuffle=False)
print(skfold)
# for train_index, val_index in cv.split(X_train):
